



function showCalendar() {
    var x = document.getElementById("calendar");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

  function showGuestForm() {
    var x = document.getElementById("choose_guests");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

